""" While Loop """
"""
Syntax:
Variable name = value
While condition:
print()
variable name increment
"""

i = 0
while i < 10:
    print(i) # we can also use end=" " to print the output in same line
    i = i + 1

j = 0
while j < 10:
    print("Hello world!", j)
    j = j + 1

""" Break """

k = 1
while k < 10: # 1 to 9
    print(k)
    k = k + 1
    if k == 5:
        break



